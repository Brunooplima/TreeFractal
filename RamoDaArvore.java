public class RamoDaArvore {
    int x1;
    int y1;
    int x2;
    int y2;
    int nivel;

    RamoDaArvore(int x1, int x2, int y1, int y2, int nivel) {
        this.x1 = x1;
        this.x2 = x2;
        this.y1 = y1;
        this.y2 = y2;
        this.nivel = nivel;

    }
}
